package ex01

import "fmt"

func shortDeclarationExercise() {
	//assign values to variables
	age := 42
	person := "James Bond"
	bald := true

	//print values using single print statement
	fmt.Println(age)
	fmt.Println(person)
	fmt.Println(bald)

	//print values using multiple print statements
	fmt.Println(age, person, bald)
}
